export function detectLanguage() {
  if (LANGUAGES.includes(window.INITIAL_LANGUAGE)) return window.INITIAL_LANGUAGE;

  const pathLang = window.location.pathname.split("/").filter(Boolean).at(-1);
  if (LANGUAGES.includes(pathLang)) return pathLang;

  const queryLang = new URLSearchParams(window.location.search).get("lang");
  if (LANGUAGES.includes(queryLang)) return queryLang;

  const savedLang = localStorage.getItem("midiTrainerLanguage");
  if (LANGUAGES.includes(savedLang)) return savedLang;

  const browserLangs = navigator.languages?.length ? navigator.languages : [navigator.language];
  for (const language of browserLangs) {
    const short = language.toLowerCase().slice(0, 2);
    if (LANGUAGES.includes(short)) return short;
  }

  const timezone = Intl.DateTimeFormat().resolvedOptions().timeZone || "";
  if (timezone.includes("Yerevan")) return "hy";
  if (timezone.includes("Berlin")) return "de";
  if (timezone.includes("Madrid")) return "es";
  if (timezone.includes("Moscow")) return "ru";

  return "en";
}

export function applyLanguage(language) {
  const lang = LANGUAGES.includes(language) ? language : "en";
  const copy = translations[lang];
  document.documentElement.lang = copy.htmlLang;
  document.title = copy.pageTitle;
  setMeta("description", copy.description);
  setMetaProperty("og:locale", copy.locale);
  setMetaProperty("og:title", copy.title);
  setMetaProperty("og:description", copy.description);
  setMetaName("twitter:title", copy.title);
  setMetaName("twitter:description", copy.description);

  for (const element of document.querySelectorAll("[data-i18n]")) {
    element.textContent = copy[element.dataset.i18n] || "";
  }

  for (const element of document.querySelectorAll("[data-i18n-aria]")) {
    element.setAttribute("aria-label", copy[element.dataset.i18nAria] || "");
  }

  const select = document.querySelector("#languageSelect");
  if (select) select.value = lang;

  const structuredData = document.querySelector("#structuredData");
  if (structuredData) {
    structuredData.textContent = JSON.stringify({
      "@context": "https://schema.org",
      "@type": "WebApplication",
      name: copy.title,
      description: copy.description,
      applicationCategory: "MusicApplication",
      operatingSystem: "Any",
      url: "https://john1912-7.github.io/midi-piano-trainer/",
      inLanguage: copy.htmlLang,
      offers: {
        "@type": "Offer",
        price: "0",
        priceCurrency: "USD",
      },
    });
  }

  localStorage.setItem("midiTrainerLanguage", lang);
  return lang;
}

export function languageUrl(language) {
  const lang = LANGUAGES.includes(language) ? language : "ru";
  const parts = window.location.pathname.split("/").filter(Boolean);
  const hasRepoPath = parts.includes("midi-piano-trainer");
  const base = hasRepoPath ? "/midi-piano-trainer/" : "/";
  return `${base}${lang}/`;
}

export function t(language, key) {
  return translations[language]?.[key] || translations.en[key] || key;
}

function setMeta(name, content) {
  const element = document.querySelector(`meta[name="${name}"]`);
  if (element) element.setAttribute("content", content);
}

function setMetaName(name, content) {
  let element = document.querySelector(`meta[name="${name}"]`);
  if (!element) {
    element = document.createElement("meta");
    element.setAttribute("name", name);
    document.head.append(element);
  }
  element.setAttribute("content", content);
}

function setMetaProperty(property, content) {
  let element = document.querySelector(`meta[property="${property}"]`);
  if (!element) {
    element = document.createElement("meta");
    element.setAttribute("property", property);
    document.head.append(element);
  }
  element.setAttribute("content", content);
}
