export function detectLanguage() {
  if (LANGUAGES.includes(window.INITIAL_LANGUAGE)) return window.INITIAL_LANGUAGE;

  const pathParts = window.location.pathname.split("/").filter(Boolean);
  const pathLang = pathParts.find((part) => LANGUAGES.includes(part));
  if (LANGUAGES.includes(pathLang)) return pathLang;

  const queryLang = new URLSearchParams(window.location.search).get("lang");
  if (LANGUAGES.includes(queryLang)) return queryLang;

  const savedLang = localStorage.getItem("midiTrainerLanguage");
  if (LANGUAGES.includes(savedLang)) return savedLang;

  const browserLangs = navigator.languages?.length ? navigator.languages : [navigator.language];
  for (const language of browserLangs) {
    const short = language.toLowerCase().slice(0, 2);
    if (LANGUAGES.includes(short)) return short;
  }

  const timezone = Intl.DateTimeFormat().resolvedOptions().timeZone || "";
  if (timezone.includes("Yerevan")) return "hy";
  if (timezone.includes("Berlin")) return "de";
  if (timezone.includes("Madrid")) return "es";
  if (timezone.includes("Moscow")) return "ru";

  return "en";
}

